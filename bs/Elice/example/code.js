function timesTable(n) {
    if(n == 2){
        for(var i = 1; i<10; i++){
            document.write("2 " + "x" + i + "=" + i*2);
            document.write('<br>');
        }
    }
    if(n == 3){
        for(var i = 1; i<10; i++){
            document.write("2 " + "x" + i + "=" + i*2);
            document.write('<br>');
        }
    }
}
timesTable(2);
timesTable(3);

